import java.util.Scanner;
public class sedang2 {
        public static void main(String[] args) {
            Scanner in = new Scanner(System.in);
            System.out.println("Masukkan Jari jari:");
            double jariJari = in.nextDouble();
            System.out.println("Masukkan tinggi tabung:");
            double tinggiTabung = in.nextDouble();
            
            double volume = volumetabung(jariJari, tinggiTabung);
            double luas = luaslingkaran(jariJari);
            System.out.println("Luas lingkaran adalah"+luas);
            System.out.println("Volume tabung adalah:"+ volume);
                }
    
        public static double volumetabung(double r, double t){
            double phi = 3.14;
            double volume = phi * r *r * t;
            return volume;
        }
        public static double luaslingkaran(double r){
            double phi = 3.14;
            double luas = phi * r * r;
            return luas;
        }
    }
































































































































































































































































































































































































































    